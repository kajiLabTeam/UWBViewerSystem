                Text(self.device.name)
                    .font(.subheadline)
                    .fontWeight(.medium)

                Text(self.device.id)
                    .font(.caption)
                    .foregroundColor(.secondary)

                if let position {
                    Text("‰ΩçÁΩÆ: (\(Int(position.x)), \(Int(position.y)))")
                        .font(.caption2)
                        .foregroundColor(.blue)
                }

                if let rotation {
